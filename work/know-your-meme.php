    <section class="solution paragraph c">
     <h3>Solution</h3> 
     <p class="medium serif">Know Your Meme was built by Jamie Wilkinson and myself at Rocketboom R&amp;D. Numerous features have been built to transform the site from a simple wiki into a community-based web portal for internet memes and culture. Even with the on-and-off cycle of R&amp;D work, simple design and development sprints have allows us to transform Know Your Meme into a very important web platform for internet memes and culture.</p>
     <p class="medium serif">The design of the website, while stark &amp; plain, is very reminiscent of older website designs. This style of design blends old-school internet culture with the expected web design styles of other important internet culture sites, such as Macrochan, 4chan, YTMND, and Wikipedia, making the site the perfect playground for our audience of 13-34 year olds. Fun tweaks, secret key combos, texts, and images have also contributed to the growth of the community.</p>
     <p class="medium serif">Built using Ruby on Rails, the website receives enormous amounts of daily traffic, requiring a fast content delivery network to serve images and static files, while extensive page cacheing allows the site to run quickly and smoothly.</p>
     <p class="medium serif">This project has also required the creation of the Know Your Meme Shop, including the creation of products, site development, inventory management, and order fulfillment through third-party solutions (Shopify &amp; Shipwire). Work also required helping develop the required ad delivery network for the management and serving of site ads within the site, a task that requires management and concern with monetization and community outreach to find acceptable solutions for everyone. Tumblr blog design, Twitter page design, iPhone application design, book layout design, and meme API development have also been required for this project.</p>
