     <h3>Statement</h3>
     <p class="medium serif">There is a direction relationship between essence and homelessness, in philosophical terminology. Franz Rosenzweig, in <em>Understanding the Sick and the Healthy</em>, notably discussed one argument to this effect, as asking what is the link between the object and the object in regards to the name and essence. Martin Heidegger, in <em>Pathways</em>, assists the other argument in this relationship, "Homelessness is the symptom of the oblivion of being". Much like Rene Magritte's <em>The Treachery of Images</em>, the visual representation notes the butter "is not today's butter." Instead, it states that it is not actual butter, but the idealization of 'butter'. It must clearly be distinguished that this piece attempts to describe the relationship between essence and homelessness through visual citations and explorable materials. By presenting the piece in a plurality, the idea of homelessness is personified. The inclusion of the depiction on repositionable adhesive material, further exaggerates the homelessness relationship by denying its current ability to have a "home" (a point of original reference).</p>
    </section>

    <section class="explanation paragraph c">
     <h3>Explanation</h3>
     <p class="medium serif">"The idea – that of a connecting link – is closely bound up with the general concept of "[the object] in itself," which, as we have seen, sometimes does act as a connecting link," wrote Rosenzweig in 1922. The link, an established understanding of what is given by a name and its concept, bonds the distinctions between the ideal and the actual.</p>
     <p class="medium serif">This is not a spectacular idea in our modern society. Rene Magritte has taken ahold of this idea in The <em>Treachery of Images</em>. His statement, "this is not a pipe," accurately depicts the notion presented by Rosenzweig. Visually representing a pipe with the statement above it expertly demonstrates what is seen by the viewer is not the actual object. Instead, it is the idea of the object, not the "object in itself." In a simplified statement, Rosenzweig writes, "The intermittent link of "[the object] in itself" may be give, but the example of a sudden decision to [understand] shows that it does not have to exist." The pipe, in Magritte's case, does not need to be a physical object to understand it 'essentially.'</p>
     <p class="medium serif">Rosenzweig too uses examples to demonstrate his argument, and can explain the distinction in a very simple statement. "The [object] remembered, the [object] desired, and the [object] [itself] are not the same."</p>
     <p class="medium serif">Lets now go back and understand what the specific object in this pice represents. Rosenzweig's fine example is not some elaborate, scientific object. Instead, it is a common consumer item, used for centuries by most people–butter. Yes, common, everyday butter. What necessarily changes when you replace '[object'] with 'butter' in the above paragraphs? Think about it, "if we consider the matter without prejudice, we observe that after this change has taken place, nothing but the word 'butter' remains the same." Yet what has changed? It is not necessarily the proposition, but the perceived concept. The 'marriage in itself' is not the same as the 'butter in itself', and is not the same in any case expect for itself.</p>
     <p class="medium serif">Once more, what connections do these metaphysical propositions have with the art piece? Analyze this statement mentioned earlier: "the butter remembered, the butter desired, and the butter [itself] are not the same." Each of the three statements give a different idea, in that the their only connection is the word 'butter.'</p>
     <p class="medium serif">The establishment of reason behind the object and corresponding text should be  understandable (or at least acceptable). However, the message are yet to be explained. Surprisingly, there is a direct association between visual depiction and the message to be explained.</p>
     <p class="medium serif">To note another level of aesthetic reasoning, we must address the medium to which this piece is presented. Using repositionable adhesive material, such as magnets and vinyl sticker paper, to affix to particular objects conveys a specific, literal message. It is a cry for a home–"help, I'm homeless!" Since it can never be permanently affixed (without alteration) to any surface, it can never have a residence to live. This association is a direct citation on Heidegger's homelessness idea, where we are to believe nothing exists as given, such that in relation to an object, the object in question is not as is given to us.</p>
