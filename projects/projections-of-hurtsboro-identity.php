    <section class="explanation paragraph c">
     <h3>Explanation</h3>
     <p class="medium serif"><em>Projections of Hurtsboro Identity</em> was a collaboration between Amanda Foran and myself as a student project for Auburn University. The Community Arts special topics course, taught by visiting professor Suzanne Hagood, was given the task of exploring and practicing relational art-making and new genre public art, with the goal of developing understanding of Russell County, Alabama, and to work closely with the community to design and help make the artworks. Russell County is part the Black Belt, and made up of small communities facing the challenges of rural decline, an under-financed education system, and high unemployment.</p>
     <p class="medium serif">Amanda and I wanted to bring something unique to the town of Hurtsboro, Alabama (popl. 592) by giving the chance for residents to creatively express themselves and their views "out loud" with members of their community. After much thought and research, we stumbled upon <a href="http://graffitiresearchlab.com/?page_id=76" target="_blank">L.A.S.E.R Tag</a> by the Graffiti Research Lab. Noting that the G.R.L. uses its projection system to allow graffiti writers to create large, innocuous works, we determined this system would be the best method to allow the citizens of Hurtsboro the unique, out-loud experience we desired to create.</p>
     <p class="medium serif">We performed our piece on the main downtown strip of Hurtsboro on Sat, April 19 2007, in combination with an art exhibition by other members of the class and a large town performance event put together by the local arts council. </p>
     <p class="medium serif">Amanda and I also took the project to the 4-H Club in Phenix City, Alabama and demonstrated the project at the Coleman Arts Center in York, Alabama.</p>
