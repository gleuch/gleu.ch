     <h3>Statement</h3>
     <p class="medium serif">Collective identity of a small town can be difficulty to describe. Often the past, present, or future defines a place and those who live in it, with or without their consent. By interviewing community members and discussing the town identity of Hurtsboro, Alabama, there is hope to establish an even stronger sense of community by finding common ground among the inhabitants.</p>
     <p class="medium serif">Much of the identity of a place resides in the people who live there. By illuminating the history with a street art-based event in which members of the town used projected lasers to make drawings on buildings, the event created a bond in which people were able to express themselves in a public forum under the pretext of creativity and play. The documentation video and photographic slide show shared with the public expands the dialogue to people outside the community in order to facilitate connections between beliefs and understandings of Hurtsboro, uncover the identity of a small town, and help form ideas for the future.</p>
